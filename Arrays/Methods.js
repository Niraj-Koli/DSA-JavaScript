const arr = ["a", "b", "c", "d"];
console.log(arr);

// Push //

arr.push("e");
console.log(arr);

// Pop //

arr.pop();
console.log(arr);

// Shift //

arr.shift();
console.log(arr);

// Unshift //

arr.unshift("x");
console.log(arr);

// Splice //

arr.splice(2, 0, "y");
console.log(arr);

arr.splice(3, 1);
console.log(arr);
